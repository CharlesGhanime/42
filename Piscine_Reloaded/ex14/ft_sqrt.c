int		ft_sqrt(int nb)
{
	int i;
	int sqrt;

	i = 1;
	sqrt = 0;
	while (nb / i != i && i < nb / 2)
	{
		i++;
	}
	if (i == nb / i && nb % i == 0)
		return (i);
	else
		return (0);
}
